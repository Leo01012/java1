public class DataTypeInspector {
    public static void main(String[] args){
        byte byteValue = 100;
        System.out.println("byte value:\t\t" + byteValue);
        short shortValue = 30000;
        System.out.println("short value:\t\t" + shortValue);
        int intValue = 100000;
        System.out.println("int value:\t\t" + intValue);
        long longValue = 15000000000L;
        System.out.println("long value:\t\t" + longValue);
        float floatValue = 5.75f;
        System.out.println("float value:\t\t" + floatValue);
        double doubleValue = 19.99;
        System.out.println("double value:\t\t" + doubleValue);
        char charValue = 'A';
        System.out.println("char value:\t\t" + charValue);
        boolean booleanValue = true;
        System.out.println("boolean value:\t\t" + booleanValue);
    }
}

